package kr.happyjob.study.classroom.model;

public class SDayoffModel {
	
	private int dayoff_no;
	
	private String dayoff_date;
	
	private String dayoff_detail;

	public int getDayoff_no() {
		return dayoff_no;
	}

	public void setDayoff_no(int dayoff_no) {
		this.dayoff_no = dayoff_no;
	}

	public String getDayoff_date() {
		return dayoff_date;
	}

	public void setDayoff_date(String dayoff_date) {
		this.dayoff_date = dayoff_date;
	}

	public String getDayoff_detail() {
		return dayoff_detail;
	}

	public void setDayoff_detail(String dayoff_detail) {
		this.dayoff_detail = dayoff_detail;
	}
	
}
