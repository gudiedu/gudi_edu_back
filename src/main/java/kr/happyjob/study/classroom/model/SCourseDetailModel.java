package kr.happyjob.study.classroom.model;

public class SCourseDetailModel {
	
	private int course_detail_no;
	private int course_no;
	private int course_detail_week_no;
	private String course_detail_goal;
	private String course_detail_content;
	
	public int getCourse_detail_no() {
		return course_detail_no;
	}
	public void setCourse_detail_no(int course_detail_no) {
		this.course_detail_no = course_detail_no;
	}
	public int getCourse_no() {
		return course_no;
	}
	public void setCourse_no(int course_no) {
		this.course_no = course_no;
	}
	public int getCourse_detail_week_no() {
		return course_detail_week_no;
	}
	public void setCourse_detail_week_no(int course_detail_week_no) {
		this.course_detail_week_no = course_detail_week_no;
	}
	public String getCourse_detail_goal() {
		return course_detail_goal;
	}
	public void setCourse_detail_goal(String course_detail_goal) {
		this.course_detail_goal = course_detail_goal;
	}
	public String getCourse_detail_content() {
		return course_detail_content;
	}
	public void setCourse_detail_content(String course_detail_content) {
		this.course_detail_content = course_detail_content;
	}
	

}
