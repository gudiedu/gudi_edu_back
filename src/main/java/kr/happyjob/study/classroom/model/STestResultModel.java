package kr.happyjob.study.classroom.model;

public class STestResultModel {

	private int result_no;
	private String loginID;
	private int course_no;
	private int result_score;
	public int getResult_no() {
		return result_no;
	}
	public void setResult_no(int result_no) {
		this.result_no = result_no;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public int getCourse_no() {
		return course_no;
	}
	public void setCourse_no(int course_no) {
		this.course_no = course_no;
	}
	public int getResult_score() {
		return result_score;
	}
	public void setResult_score(int result_score) {
		this.result_score = result_score;
	}
	
	
	
}
